package com.sdnu.study.domain;

public class PinyinTableItem {
	private String mChar=null;
	private String pinyin=null;
	private String hanzi=null;
	private String picPath=null;
	private String duYin=null;
	public String getmChar() {
		return mChar;
	}
	public void setmChar(String mChar) {
		this.mChar = mChar;
	}
	public String getPinyin() {
		return pinyin;
	}
	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}
	public String getHanzi() {
		return hanzi;
	}
	public void setHanzi(String hanzi) {
		this.hanzi = hanzi;
	}
	public String getPicPath() {
		return picPath;
	}
	public void setPicPath(String picPath) {
		this.picPath = picPath;
	}
	public String getDuYin() {
		return duYin;
	}
	public void setDuYin(String duYin) {
		this.duYin = duYin;
	}
}
