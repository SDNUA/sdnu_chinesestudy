package com.sdnu.study.domain;

public class BiHuaItem {
	
	private String bihua;
	private String bihuaPy;
	/**
	 * @return the bihua
	 */
	public String getBihua() {
		return bihua;
	}
	/**
	 * @param bihua the bihua to set
	 */
	public void setBihua(String bihua) {
		this.bihua = bihua;
	}
	/**
	 * @return the bihuaPy
	 */
	public String getBihuaPy() {
		return bihuaPy;
	}
	/**
	 * @param bihuaPy the bihuaPy to set
	 */
	public void setBihuaPy(String bihuaPy) {
		this.bihuaPy = bihuaPy;
	}
	

}
